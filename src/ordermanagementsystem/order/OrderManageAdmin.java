    private void performFileRelatedTask() {
        StringBuilder stringBuilder = new StringBuilder();
        int num = 1;
        for (OrderLine orderLine : orderService.getAllOrders()) {
            stringBuilder.append(num)
                    .append("\t\t")
                    .append(orderLine.getOrderID())
                    .append(" \t\t")
                    .append(orderLine.getName())
                    .append(" \t\t            ")
                    .append(orderLine.getToppings())
                    .append(" \t\t\t")
                    .append(orderLine.getQuantity())
                    .append(" \t\t")
                    .append(orderLine.getPrice())
                    .append("\n");
            num++;
        }
        reciptArea.setText(stringBuilder.toString());
    }

