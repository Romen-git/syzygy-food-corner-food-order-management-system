class CompleteHandler implements OrderHandler {

    private OrderHandler next;

    @Override
    public void handleOrder(OrderLine order, OrderManageAdmin orderManageAdmin) {
        JOptionPane.showMessageDialog(orderManageAdmin, "Order is being completed.");

        if (next != null) {
            next.handleOrder(order, orderManageAdmin);
        }
    }

    @Override
    public void setNext(OrderHandler next) {
        this.next = next;
    }
}
