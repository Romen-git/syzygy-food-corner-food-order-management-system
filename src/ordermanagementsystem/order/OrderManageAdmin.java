    private void completeOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeOrderButtonActionPerformed

        OrderHandler acceptHandler = new AcceptanceHandler();
        OrderHandler cookHandler = new CookingHandler();
        OrderHandler packHandler = new PackingHandler();
        OrderHandler completeHandler = new CompleteHandler();

        acceptHandler.setNext(cookHandler);
        cookHandler.setNext(packHandler);
        packHandler.setNext(completeHandler);

        acceptHandler.handleOrder(orderService.getFirstOrder(), this);

        SyzygyGroup syzygyGroup = new SyzygyGroup("Syzygy Group");

        ChatMediator chatMediator = new ChatMediator(syzygyGroup, chatManagement);
        syzygyGroup.setMediator(chatMediator);

        syzygyGroup.sendMessage("Your order is being completed. It will be delivered to your doorstep..");

        orderService.clearOrder();
        this.reciptArea.setText("");
        performFileRelatedTask();

        jButton3.setEnabled(true);
        jButton2.setEnabled(true);
        jButton1.setEnabled(true);
    }//GEN-LAST:event_completeOrderButtonActionPerformed

